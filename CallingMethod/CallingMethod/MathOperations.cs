using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CallingMethod
{   
    //Calling Method Assignment:
    // Define a class to perform math operations
    public class MathOperations
    {
         // Method_One to double a given number
          public int DoubleNumber(int num) 
          {
                // Return the input number multiplied by 2
                return num * 2;
          }

          // Method_Two to square a given number
          public int SquareNumber(int num)
          {
                // Return the input number squared
                return num * num;
          }

          // Method_Three to add 5 to a given number
          public int AddFive(int num)
          {
                // Return the input number plus 5
                return num + 5;
          }

        
    }
}

